package com.jamesgallicchio.s4bf.bf;

public class BFByteFormat {
    public static final byte IN         = 0b0000001;
    public static final byte OUT        = 0b0000010;
    public static final byte UP         = 0b0000100;
    public static final byte DOWN       = 0b0001000;
    public static final byte RIGHT      = 0b0010000;
    public static final byte LEFT       = 0b0100000;
    public static final byte BRAC_LEFT  = 0b1000000;
    public static final byte BRAC_RIGHT = -0b10000000;
}
